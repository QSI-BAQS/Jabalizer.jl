    # println("oz_qubit is $(oz_qubit) and slippers_qubit is $(slippers_qubit) and kansas_qubit is $(kansas_qubit)!\n\n\n")
    pauli_tracker.frames_new_qubit(pauli_tracker_data.frames, UInt(slippers_qubit) - 1)
